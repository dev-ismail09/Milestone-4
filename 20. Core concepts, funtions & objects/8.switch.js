// switch
const color = 'yellow';

switch (color) {
    case 'green':
        console.log('You are my green friend');
        break;
    case 'red':
        console.log('You are my red friend');
        break;
    case 'black':
        console.log('You are my black friend');
        break;
    case 'white':
        console.log('You are my white friend');
        break;
    case 'yellow':
        console.log('You are my yellow friend');
        break;
    default:
        console.log('ohh nooo');
}